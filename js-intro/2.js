/* 
  *typeof
  - mengembalikan string yang menunjukkan tipe data dari sebuah nilai.
  
  Syntax:
  typeof operand

*/

console.log(typeof 46); // Output: number
console.log(typeof NaN); // Output: number
console.log(typeof "jackie chan"); // Output: string
console.log(typeof ""); // Output: string
console.log(typeof true); // Output: boolean
console.log(typeof false); // Output: boolean
console.log(typeof undefined); // Output: undefined
console.log(typeof age); // Output: undefined, undeclared variable
console.log(typeof []); // Output: object
console.log(typeof [1, 2, 3]); // Output: object
console.log(typeof {}); // Output: object

