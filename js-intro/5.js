let number = 2;
console.log(number); // 2

//? Bagaimana cara menambahkan 3 ke nilai variable number yang telah dideklarasikan?
number = number + 3;
console.log(number); // 5

/* 

  *Shorthand (Penulisan Singkat)
  Ada shorthand untuk code seperti number = number + 10. 
  Shorthand ini sering digunakan, jadi coba ingat format-nya sesuai contoh di bawah!

*/
number += 10; // number = number + 10
console.log(number); // 15

number -= 5; // number = number - 5
console.log(number); // 10

number *= 2; // number = number * 2
console.log(number); // 20

number /= 5; // number = number / 5
console.log(number); // 4

number %= 5; // number = number % 5
console.log(number); // 4
